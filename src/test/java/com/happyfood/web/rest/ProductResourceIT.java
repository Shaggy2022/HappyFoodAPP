    @Test
    @Transactional
    void createProduct() throws Exception {
        int databaseSizeBeforeCreate = productRepository.findAll().size();
        // Create the Product
        ProductDTO productDTO = productMapper.toDto(product);
        restProductMockMvc
            .perform(post(ENTITY_API_URL).contentType(MediaType.APPLICATION_JSON).content(TestUtil.convertObjectToJsonBytes(productDTO)))
            .andExpect(status().isCreated());

        // Validate the Product in the database
        List<Product> productList = productRepository.findAll();
        assertThat(productList).hasSize(databaseSizeBeforeCreate + 1);
        Product testProduct = productList.get(productList.size() - 1);
        assertThat(testProduct.getSerial()).isEqualTo(DEFAULT_SERIAL);
        assertThat(testProduct.getRequiredProduct()).isEqualTo(DEFAULT_REQUIRED_PRODUCT);
        assertThat(testProduct.getPrice()).isEqualByComparingTo(DEFAULT_PRICE);
    }

